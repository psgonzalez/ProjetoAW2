    <h1>Como surgiu o nosso site?</h1>

    <p>  Nos primórdios havia a paz, a agressividade só era dada aos mandos da natureza do ser, porém jamais com crueldade. Não havia nações, exércitos, miséria ou tortura, o individuo se entendia com o seu coletivo e juntos eram um só, sem espaço para discriminações e segregações. Porém um macaco bonobo primário desrespeitou a Mãe Natureza e começou a desenvolver a ferramenta mais maligna encontrada na singularidade universal, a consciência humana. </p>

    <p>  A Mãe Natureza tinha entregue tudo o que os seus filhos precisavam para uma experiência terrena completa e transcendente com os seus iguais, mas com a chegada da consciência humana houve a contemplação empírica e racional da vida, algo que nem a própria Mãe Natureza em sua complexidade como um todo se atreve a experienciar por saber que o mal habita neste exercício.</p>

    <p>O macaco primário conhecido como Adão muda sua forma física, comportamental e moral amaldiçoando todos os seus descendentes a completas inutilidades como trabalho, estudo, estética, metafísica, roupas, cálculos, religiões, ideologias, higiene básica, trânsito, ontologia e todas as mais sórdidas criações da sociedade, das do período antigo às contemporâneas, desde suas leis a ideia de sucesso. Seu maior fetiche chamado progresso, algo completamente abstrato que pode se ligar com a ciência, é apenas mais uma ferramenta de morte usada contra seus iguais macacos depilados. </p>

    <p>Macacos depilados se acham inteligentes gastando sua vida em futilidades para outros macacos depilados, onde a única coletividade que encontram é que ambos caminham juntos para o apocalipse que eles mesmos travaram contra a Mãe Natureza.</p>

    <p>Porém ainda há tempo de salvar o seu corpo e vida(não, não há algo como a ideia de “alma”), toda criação e ideia posterior ao período neolítico deve ser destruído, não é necessário a criação de um mundo novo pois a partir dos escombros da sociedade a natureza, o modo padrão como deve ser, se ira se instaurar. </p>

